def _legal_actions(observation):
    """Return the list of legal actions from the cg observation.
    SEAM: replace with the cg accessor (commonly observation has a legal-action
    list or the env exposes one)."""
    for key in ("legal_actions", "legalActions", "actions", "valid_actions"):
        try:
            val = observation[key]
            if val:
                return list(val)
        except Exception:
            pass
    return []


def _score_action(observation, action):
    """Heuristic score for a candidate action.
    SEAM: map cg action/state fields to these checks for full strength."""
    a = str(action).lower()
    score = 0.0
    if "attack" in a:
        score += 3.0          # pressure / prizes
    if "evolve" in a:
        score += 1.6          # board development
    if "attach" in a or "energy" in a:
        score += 1.2          # power up the attacker
    if "supporter" in a or "draw" in a or "search" in a:
        score += 0.8          # dig for setup
    if "ability" in a:
        score += 0.6
    if "retreat" in a:
        score += 0.2
    if "pass" in a or "end" in a:
        score -= 1.0          # don't pass while useful plays remain
    return score


def agent(observation, configuration=None):
    """Kaggle Environments entrypoint: pick one legal action."""
    actions = _legal_actions(observation)
    if not actions:
        return None  # let the environment apply a forced no-op / pass
    best, best_score = actions[0], float("-inf")
    for act in actions:
        s = _score_action(observation, act)
        if s > best_score:
            best, best_score = act, s
    return best


