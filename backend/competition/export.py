"""Exports for the two PTCG AI Battle Challenge tracks.

* ``build_sim_bundle`` produces a **submission.tar.gz** for the *Simulation*
  category with ``main.py`` and ``deck.csv`` at the **top level** (plus a short
  README). The competition runs the bundle on Kaggle Environments; you add the
  ``cg/`` library (from the ``cg-lib`` dataset) and rebuild with
  ``tar -czvf submission.tar.gz *``.

* ``build_strategy_writeup`` produces a Markdown **Writeup** for the *Strategy*
  category, structured around the judging rubric (Model 70% / Deck 20% /
  Report 10%) and kept within the 2000-word limit.

Card-id mapping: the competition identifies cards by its own *Card ID* (see
``EN Card Data.csv``). If that file is available to the backend (env var
``PTCG_CARD_DATA`` or ``backend/data_store/EN Card Data.csv``) we map our deck's
card names to official Card IDs in ``deck.csv``; otherwise the column is left
blank with a note so you can fill it in.
"""
