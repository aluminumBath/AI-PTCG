def _deck_csv(deck_cards, name_to_id: dict[str, str]) -> str:
    buf = io.StringIO()
    w = csv.writer(buf)
    w.writerow(["card_id", "card_name", "count"])
    unmapped = 0
    for name, count in _deck_counts(deck_cards):
        cid = name_to_id.get(name.lower(), "")
        if not cid:
            unmapped += 1
        w.writerow([cid, name, count])
    text = buf.getvalue()
    if unmapped:
        text = (
            "# NOTE: 'card_id' values below are blank because the official "
            "EN Card Data.csv\n"
            "# was not available when this file was generated. Fill each card_id "
            "from\n# the competition's card dataset (match on card_name).\n"
        ) + text
    return text


def _readme(deck_id: str, agent_label: str, total: int) -> str:
    return f"""# PTCG AI Battle — Simulation submission

Deck: **{deck_id}**  ·  Strategy: **{agent_label}**  ·  {total} cards

## Contents (top level)
- `main.py` — the agent entrypoint (`agent(observation, configuration)`).
- `deck.csv` — the 60-card deck (one row per distinct card with its count).
- `README.md` — this file.

## To create a valid submission
1. Add the competition game library `cg/` (from the **cg-lib** dataset) at the
   top level, next to `main.py`.
2. Map any blank `card_id` values in `deck.csv` to official **Card ID**s from
   `EN Card Data.csv` (match on `card_name`).
3. Bind the observation/action seam in `main.py` to the `cg` schema.
4. Build the archive from the folder contents (not the folder):

   ```bash
   tar -czvf submission.tar.gz *
   ```

5. Submit `submission.tar.gz` on the competition's Simulation page.
"""


def build_sim_bundle(deck_id: str, agent_id: str, agent_label: str,
                     deck_resolver: Callable[[str], list]) -> bytes:
    deck_cards = deck_resolver(deck_id)
    name_to_id = load_official_name_to_id()

    files = {
        "main.py": _main_py(deck_id, agent_id, agent_label),
        "deck.csv": _deck_csv(deck_cards, name_to_id),
        "README.md": _readme(deck_id, agent_label, len(deck_cards)),
    }

    out = io.BytesIO()
    with tarfile.open(fileobj=out, mode="w:gz") as tar:
        for arcname, content in files.items():
            data = content.encode("utf-8")
            info = tarfile.TarInfo(name=arcname)   # top level, not nested
            info.size = len(data)
            info.mtime = int(time.time())
            info.mode = 0o644
            tar.addfile(info, io.BytesIO(data))
    return out.getvalue()


