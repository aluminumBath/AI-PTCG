# --------------------------------------------------------------------------- #
# Official card-data lookup (optional)
# --------------------------------------------------------------------------- #
def _card_data_path() -> Optional[str]:
    cand = [
        os.environ.get("PTCG_CARD_DATA", ""),
        os.path.join(_BACKEND_ROOT, "data_store", "EN Card Data.csv"),
        os.path.join(_BACKEND_ROOT, "data_store", "en_card_data.csv"),
    ]
    for p in cand:
        if p and os.path.isfile(p):
            return p
    return None


def load_official_name_to_id() -> dict[str, str]:
    """Map normalized official Card Name -> Card ID, if the dataset is present."""
    path = _card_data_path()
    if not path:
        return {}
    out: dict[str, str] = {}
    try:
        with open(path, newline="", encoding="utf-8-sig") as fh:
            reader = csv.DictReader(fh)
            for row in reader:
                # tolerate header variations
                name = (row.get("Card Name") or row.get("card_name") or row.get("Name") or "").strip()
                cid = (row.get("Card ID") or row.get("card_id") or row.get("ID") or "").strip()
                if name and cid:
                    out[name.lower()] = cid
    except Exception:
        return {}
    return out


def _deck_counts(deck_cards) -> list[tuple[str, int]]:
    """[(card_name, count)] preserving first-seen order."""
    names = [c.card.name if hasattr(c, "card") else c.name for c in deck_cards]
    counts = Counter(names)
    seen, ordered = set(), []
    for n in names:
        if n not in seen:
            seen.add(n)
            ordered.append((n, counts[n]))
    return ordered


